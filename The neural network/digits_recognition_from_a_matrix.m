P = [zero(:) unu(:) doi(:) trei(:) patru(:) cinci(:) sase(:) sapte(:) opt(:) noua(:)];
clear zero unu doi trei patru cinci sase sapte opt noua;

targets = eye(10);

%% CREATE THE NETWORK

S1 = 10; 
[R, ~] = size(P); 
[S2, Q] = size(targets); 
net = newff(minmax(P), [S1 S2], {'logsig' 'logsig'}, 'traingdx'); 
net.LW{2, 1} = net.LW{2, 1} * 0.01; 
net.b{2} = net.b{2} * 0.01;
net.trainParam.epochs = 100;
net.trainParam.goal = 0.6;
net.trainParam.mc = 0.95;
net.performFcn = 'sse';

%% TRAIN WITH NOISE

P1 = P + randn(R, Q) * 0.1;
P2 = P + rand(R, Q) * 0.2;
P1(P1 < 0) = 0;
P1(P1 > 1) = 1;
P2(P2 < 0) = 0;
P2(P2 > 1) = 1;

PZ = [P P P1 P2];
TZ = [targets, targets, targets, targets];

net = train(net, PZ, TZ);


%% TRAIN AGAIN ON DIGITS WITHOUT NOISE

net.trainParam.epochs = 100;
net.trainParam.goal = 0.5;
net = train(net, P, targets);

%% TEST THE NETWORK

Px = []; Tx = [];
for d = 0 : 0.05 : 0.5
    for i = 1:10
        Pz = P + randn(R, Q) * d;
        Px = [Px Pz];
        Tx = [Tx targets];
    end
end

Yx = sim(net, Px);
Yx = compet(Yx);
Yx = mod(find(Yx == 1), Q);
Tx = mod(find(Tx == 1), Q);
E = numel(find((Tx ~= Yx) == 1)) / length(Tx)

%% TEST AND SHOW A DIGIT

number = round((10-1) * rand() + 1);
